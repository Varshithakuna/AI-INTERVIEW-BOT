import streamlit as st


def render_evaluation(report):

    st.markdown("<br>", unsafe_allow_html=True)

    # ==================================================
    # Header
    # ==================================================

    st.markdown("""
    <div class="card">

    <h1 style="margin-bottom:5px;">
        📊 AI Interview Report
    </h1>

    <p style="color:#94A3B8;">
        Here's your complete AI-generated interview evaluation.
    </p>

    </div>
    """, unsafe_allow_html=True)

    if report is None:

        st.warning("No evaluation available.")

        return

    if "error" in report:

        st.error(report["error"])

        st.code(report["raw_response"])

        return

    # ==================================================
    # Overall Score
    # ==================================================

    st.markdown("## 🏆 Overall Performance")

    score = report["overall_score"]

    st.progress(score / 100)

    st.markdown(
        f"""
        <h1 style="
        text-align:center;
        color:#8B5CF6;
        margin-top:10px;
        ">
        {score}%
        </h1>
        """,
        unsafe_allow_html=True
    )

    st.markdown("<br>", unsafe_allow_html=True)

    # ==================================================
    # Candidate Summary
    # ==================================================

    st.markdown("## 👤 Candidate Summary")

    col1, col2, col3, col4 = st.columns(4)

    with col1:
        st.metric(
            "Candidate",
            report["candidate_name"]
        )

    with col2:
        st.metric(
            "Role",
            report["role"]
        )

    with col3:
        st.metric(
            "Questions",
            report["questions_answered"]
        )

    with col4:
        st.metric(
            "Overall",
            f"{score}%"
        )

    st.markdown("<br>", unsafe_allow_html=True)

    # ==================================================
    # Skill Scores
    # ==================================================

    st.markdown("## 📈 Skill Assessment")

    skills = [
        ("Technical Knowledge", report["technical_score"]),
        ("Communication", report["communication_score"]),
        ("Problem Solving", report["problem_solving_score"]),
        ("Confidence", report["confidence_score"])
    ]

    for title, value in skills:

        st.markdown(f"**{title}**")

        st.progress(value / 10)

        st.caption(f"{value}/10")

    st.markdown("<br>", unsafe_allow_html=True)

    # ==================================================
    # Feedback Columns
    # ==================================================

    left, right = st.columns(2)

    with left:

        st.markdown("## ✅ Strengths")

        for item in report["strengths"]:
            st.success(item)

        st.markdown("<br>")

        st.markdown("## 💡 Suggestions")

        for item in report["suggestions"]:
            st.info(item)

    with right:

        st.markdown("## ⚠ Areas to Improve")

        for item in report["weaknesses"]:
            st.warning(item)

        st.markdown("<br>")

        st.markdown("## 🎯 Recommendation")

        st.success(report["recommendation"])

    st.markdown("<br>", unsafe_allow_html=True)

    # ==================================================
    # Footer
    # ==================================================

    st.markdown("---")

    st.caption("Generated by AI Interview Partner")